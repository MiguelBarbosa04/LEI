//        Nó(Node)
//        : Cada elemento individual em uma lista ligada é representado por um nó
//        . Um nó contém o valor do {
//            elemento 
//        }  e uma referência(ou ponteiro
//        ) para o próximo nó na lista
//        .
//
//Cabeça(Head)
//        : A referência ao primeiro nó da lista.É o ponto de entrada da lista.
//                Cauda(Tail)
//        : A referência ao último nó da lista.Em algumas implementações
//        , a cauda é opcional
//        .
//
//Para que serve um nó numa lista ligada
//        ?
//Um nó em uma lista ligada serve para armazenar o valor do {
//            elemento 
//        }  e manter uma referência ao próximo nó.É a unidade básica de dados que permite a construção de uma estrutura de lista encadeada, permitindo a conexão de elementos sequencialmente.Qual a diferença entre um array e uma lista ligada
//        ?
//
//Array:
//        Um 
//        array é uma estrutura de dados estática que armazena elementos do {
//            mesmo 
//        }  tipo em uma área de memória contígua.O acesso aos elementos é rápido por índice, mas a inserção e a remoção de elementos no meio do {
//            array 
//        }  podem ser lentas e ineficientes
//        , pois podem exigir realocação de memória
//        .
//
//Lista Ligada
//        : Uma lista ligada é uma estrutura de dados dinâmica que consiste em nós
//        , onde cada nó contém um valor e uma referência ao próximo nó
//        . A inserção e a remoção de elementos são eficientes em uma lista ligada
//        , especialmente quando se trata de adicionar ou remover elementos no meio
//        , mas o acesso direto aos elementos por índice é menos eficiente.Qual a diferença entre uma lista ligada e uma lista duplamente ligada
//        ?
//
//Lista Ligada (Simplesmente Ligada
//        ): Numa lista ligada simples, cada nó tem uma referência apenas para o próximo nó na sequência.Isso permite a travessia da lista somente em uma direção, geralmente da cabeça (início
//        ) para a cauda(fim).Lista Duplamente Ligada
//        : Em uma lista duplamente ligada
//        , cada nó tem duas referências
//        , uma para o próximo nó e outra para o nó anterior na sequência.Isso permite a travessia da lista em ambas as direções, ou seja
//        , tanto da cabeça para a cauda quanto da cauda para a cabeça
//        . A lista duplamente ligada oferece mais flexibilidade em relação à lista simplesmente ligada
//        , mas consome um pouco mais de memória devido às referências adicionais.
