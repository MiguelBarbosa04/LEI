////Complexidade das Operações: Como resultado da diferença na estrutura de dados, as complexidades de tempo para as operações variam. 
//Em ArrayStack, as operações push e pop têm complexidade O(1) devido ao acesso direto à matriz. Em LinkedStack, essas operações também têm complexidade O(1) devido ao acesso direto ao início da lista encadeada.
////
////Tamanho da Pilha: Em ArrayStack, é necessário definir um tamanho máximo para a pilha com antecedência (a menos que seja usado um redimensionamento dinâmico).
//        Em LinkedStack, não há limite teórico para o tamanho da pilha, pois a lista ligada pode crescer conforme necessário.
////
////Espaço em Memória: ArrayStack usa uma quantidade fixa de memória com base no tamanho máximo da pilha, enquanto LinkedStack usa memória dinamicamente, alocando espaço conforme novos elementos são adicionados.
////
////Alocação e Liberação de Memória: Em ArrayStack, o espaço de memória é alocado uma vez para o array com o tamanho máximo especificado e não é liberado até que o objeto seja destruído. 
//                Em LinkedStack, a memória é alocada dinamicamente para cada nó da lista ligada à medida que os elementos são adicionados e é liberada quando o nó não é mais referenciado.
////
////Uso de Índices: ArrayStack usa índices para acessar elementos na matriz, enquanto LinkedStack usa referências de nó para acessar elementos na lista ligada.
////
////Flexibilidade: LinkedStack é mais flexível em termos de tamanho da pilha, mas pode exigir um pouco mais de overhead de memória para as referências dos nós na lista ligada.
////
////Código Fonte: A implementação do código real para ArrayStack e LinkedStack é diferente, mas as interfaces e o comportamento geral (empilhar, desempilhar, verificar o topo) são semelhantes.
////
